    private void FieldSelected() {
        int car = transferir.getCaretPosition();
        String anttxt = transferir.getText().substring(0,car);
        String postxt = transferir.getText().substring(car);
        String newtxt = anttxt + "<@" + getDataBaseName(tabelaVariaveis.getSelectedIndex()) + "=" + variaveis.getValueAt(variaveis.getSelectedRow(),1) + "/>" + postxt;
        transferir.setText(newtxt);
    }

