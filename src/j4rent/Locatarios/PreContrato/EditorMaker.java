    private void FillVariaveis() {
        variaveis.setModel(tablemodel);
        
        int[] Size = {10,200,100};
        for (int i = 0; i < tablemodel.getColumnCount(); i++) {
            variaveis.getColumnModel().getColumn(i).setPreferredWidth(Size[i]);

            if (Size[i] == 0) {
                TableColumn tableColumn = variaveis.getColumn(variaveis.getColumnName(i));
                tableColumn.setPreferredWidth(0);
                tableColumn.setMinWidth(0);
                tableColumn.setMaxWidth(0);

                variaveis.getTableHeader().setReorderingAllowed(false);
                variaveis.getTableHeader().setResizingAllowed(false);
            }            
        }

        // Zera a tabela
        tablemodel.clear();
        Object[][] vars = DatabaseVariables(tabelaVariaveis.getSelectedIndex());
        for (Object[] item : vars) {
            EditorMakerClass _vars = new EditorMakerClass(item[0].toString(), item[1].toString(), item[2].toString());
            tablemodel.addRow(_vars);
        }
        variaveis.getColumnModel().getColumn(0).setPreferredWidth(0);
        TableColumn tableColumn = variaveis.getColumn(variaveis.getColumnName(0));
        tableColumn.setPreferredWidth(0);
        tableColumn.setMinWidth(0);
        tableColumn.setMaxWidth(0);

        variaveis.getTableHeader().setReorderingAllowed(false);
        variaveis.getTableHeader().setResizingAllowed(false);
                
        variaveis.getColumnModel().getColumn(1).setPreferredWidth(100);
        variaveis.getColumnModel().getColumn(2).setPreferredWidth(300);
        
        sorter = new TableRowSorter<EditorMakerModel>(tablemodel);
        variaveis.setRowSorter(sorter);

        sorter.setRowFilter(null);
        jBusca.setText("");
        jBusca.requestFocus();
    }

